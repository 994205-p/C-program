#include<stdio.h>
int main()
{
    int a=5;
    int b=3;
    int c=a+b;
    printf("addition: %d \n", c);

    int d=a-b;
    printf("subraction: %d \n", d);

    int e=a*b;
    printf("multiple: %d \n", e);

    int f=a%b;
    printf("divide: %d \n", f);

    int g=a/b;
    printf("module: %d \n", g);
}
